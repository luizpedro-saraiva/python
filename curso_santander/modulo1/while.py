# variável chamada contador e atribui o valor numérico \(0\) a ela.
contador = 0 

# O While verifica se o contador é menor que 10. 
# Se a condição for verdadeira, o código dentro do bloco do while será executado.
while contador < 10: 
    print (contador)
    
    # O valor de contador é aumentado em 
    # 1 (\(0 + 1 = 1\)). 
    # Isso é crucial para que o loop não seja infinito.
    contador += 1
